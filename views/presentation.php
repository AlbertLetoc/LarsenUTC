		<h1>L'association :</h1> Présentation
	</div>
</div>
<div class="card">
	<div class="card-content col-xs-12">
		La commission du PAE-UTC Larsen a été fondée le 26 novembre 1986 par un groupe d'étudiants ingénieurs passionés de musique. <br/>
		L’objectif de l’association est de rassembler et représenter au sein de l’UTC l’ensemble des musiciens ayant un attrait pour la pratique instrumentale/vocale de groupe, et participer à des évènements dans le but de promouvoir les musiciens UTCéens dans le cadre de l’UTC, de Compiègne ou a l’extérieur.<br />
		Larsen dispose d’une salle de répétition au rez-de-chaussée de la Maison des étudiants et utilise également le studio d’enregistrement en collaboration avec l’association Décibels pour permettre à l’ensemble des groupes de pouvoir répéter.<br />

		Beaucoup de groupes issus de LARSEN ont pu jouer lors d’évènements UTCéens : WEI, Gigot Bitume & Estu Parking, Vieilles Pipettes, Nuit Fauve, SDF, Festupic, UTCéenne, Gala, Jams LARSEN dans des bars, permanences au PIC… Ou hors UTC : Gala de l’ESCOM, ovalies, fête de la musique etc ...<br />

		Si vous souhaitez jouer dans un groupe, peu importe le style musical, contactez-nous !
